const mongoose = require("mongoose");


const userSchema = new mongoose.Schema({
    name: {
        type: String,
        unique: true,
        required: true
    },
    email: {
        type: String,
        unique: true,
        required: true
    },
    password: {
        type: String,
        required: true
    }
}, {
    timestamps: true
})

const CreateUser = mongoose.model("User", userSchema);
module.exports = CreateUser